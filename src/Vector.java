public class Vector{
	private float x; 
	private float y; 

	//returns x magnitude of the vector 
	public float getX(){
		return x;
	}

	//sets x magnitude of vector
	public void setX(float xValue){
		x = xValue;
	}
	
	//returns y magnitude of vector
	public float getY(){
		return y;
	}

	//sets y magnitude of vector
	public void setY(float yValue){
		y = yValue;
	}
}
